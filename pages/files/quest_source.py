def decypher_rail_fence(file1, file2):
    message = []
    with open(file1,'r') as f1, open (file2,'r') as f2:
        text1 = f1.read()
        text2 = f2.read()
        for i in range(len(text1)-2):
            if i % 2 == 0:
                message.append(text1[i])
                message.append(text2[i])
            else:
                message.append(text2[i])
                message.append(text1[i])
    print(*message, sep = '')


    

