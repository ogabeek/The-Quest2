def rail_fence_maker(text):
    with open('files/file1.txt', 'a') as f:
        for i in range(len(text)):
            if i % 2 == 0:
                f.write(text[i])

    with open('files/file2.txt', 'a') as f:
        for i in range(len(text)):
            if i % 2 != 0:
                f.write(text[i])

    

